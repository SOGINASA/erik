"""Конфигурация ML-модуля прогноза явки волонтёра на следующий сбор.

Здесь собраны единые константы, которые используют ВСЕ скрипты
(генерация данных, инженерия признаков, обучение, инференс):

* каталог типов мероприятий (тем) — синтетический, но совместим с backend `Theme`;
* список интересов/навыков волонтёра;
* имена признаков и пути к артефактам.

Типы мероприятий намеренно расширены относительно текущего seed
(`backend/seed.py`: eco / elderly / animals / blood / edu / trees / homeless).
Новые темы (medical / disaster / sport / culture / it) помечены как синтетические —
их можно позже добавить в таблицу `themes` на бэке и в справочник на фронте.
"""
import sys
from pathlib import Path

# На Windows консоль по умолчанию cp1251 и падает на символах вроде «─»/«→».
# Принудительно переключаем вывод на UTF-8 (все скрипты импортируют config).
if sys.platform == "win32":
    try:
        sys.stdout.reconfigure(encoding="utf-8")
        sys.stderr.reconfigure(encoding="utf-8")
    except Exception:
        pass

# ─────────────────────────────────────────────────────────────────────────────
#  Пути
# ─────────────────────────────────────────────────────────────────────────────
ML_DIR = Path(__file__).resolve().parent
ARTIFACTS_DIR = ML_DIR / "artifacts"        # сюда сохраняется обученная модель
DATA_DIR = ML_DIR / "data"                  # сюда пишутся синтетические CSV

MODEL_PATH = ARTIFACTS_DIR / "attendance_model.joblib"
METRICS_PATH = ARTIFACTS_DIR / "metrics.json"
CONFUSION_PLOT_PATH = ARTIFACTS_DIR / "confusion_matrix.png"
ROC_PLOT_PATH = ARTIFACTS_DIR / "roc_curve.png"

EVENTS_CSV = DATA_DIR / "events.csv"         # «сырой» журнал сборов (по одному на RSVP)
FEATURES_CSV = DATA_DIR / "features.csv"     # готовая матрица признаков (для отладки)

for _d in (ARTIFACTS_DIR, DATA_DIR):
    _d.mkdir(parents=True, exist_ok=True)


# ─────────────────────────────────────────────────────────────────────────────
#  Каталог типов мероприятий (тем)
#  id -> человекочитаемое название. Первые 7 совпадают с backend/seed.py,
#  остальные — синтетические, для расширения.
# ─────────────────────────────────────────────────────────────────────────────
EVENT_TYPES = {
    "eco":       "Экология",          # уборка территорий, сортировка
    "elderly":   "Помощь пожилым",
    "animals":   "Приюты для животных",
    "blood":     "Донорство крови",
    "edu":       "Образование",       # репетиторство, лекции
    "trees":     "Озеленение",
    "homeless":  "Помощь бездомным",
    # ── синтетические, будут добавлены во фронт/бэк позже ──
    "medical":   "Медпомощь",         # сопровождение в больницах, первая помощь
    "disaster":  "Помощь при ЧС",     # паводки, пожары
    "sport":     "Спортивные события",
    "culture":   "Культура и фестивали",
    "it":        "Цифровое волонтёрство",
}

EVENT_TYPE_IDS = list(EVENT_TYPES.keys())    # фиксированный порядок one-hot
N_EVENT_TYPES = len(EVENT_TYPE_IDS)


# ─────────────────────────────────────────────────────────────────────────────
#  Ответ на приглашение (RSVP). Совпадает с backend ANSWERS.
# ─────────────────────────────────────────────────────────────────────────────
ANSWERS = ("yes", "maybe", "no")


# ─────────────────────────────────────────────────────────────────────────────
#  Имена признаков модели.
#  Держим их в одном месте, чтобы train.py и inference.py собирали
#  ОДИНАКОВЫЙ вектор (нет train/serve skew).
# ─────────────────────────────────────────────────────────────────────────────

# Числовые признаки — «сколько приходил / сколько пропускал» и производные.
NUMERIC_FEATURES = [
    "events_total",          # сколько сборов было в истории (записался)
    "events_came",           # на сколько ПРИШЁЛ
    "events_missed",         # сколько ПРОПУСТИЛ
    "attendance_rate",       # came / total (со сглаживанием Лапласа)
    "reliability",           # 0..100, округлённая надёжность (как в backend User)
    "recent_came_rate",      # доля явок за последние N сборов (свежая динамика)
    "days_since_last",       # сколько дней прошло с последнего сбора (свежесть)
    "theme_total",           # сколько сборов ЭТОЙ темы было в истории
    "theme_came",            # на сколько сборов этой темы пришёл
    "theme_attendance_rate", # явка именно по этой теме (со сглаживанием)
    "interest_match",        # 1, если тема сбора входит в интересы волонтёра
    "num_interests",         # сколько тем интересно волонтёру
]

# Категориальные признаки (one-hot внутри пайплайна).
CATEGORICAL_FEATURES = [
    "answer",                # yes | maybe | no — ответ на приглашение
    "event_type",            # тип текущего мероприятия
]

ALL_FEATURES = NUMERIC_FEATURES + CATEGORICAL_FEATURES

# Целевая переменная: 1 — придёт (came), 0 — не придёт (missed).
TARGET = "came"

# Сглаживание Лапласа для attendance_rate (перекликается с alpha=3 из forecast.js).
LAPLACE_ALPHA = 3.0
# Базовая априорная явка новичка (когда истории ещё нет).
PRIOR_ATTENDANCE = 0.5
# Окно «свежей динамики» для recent_came_rate.
RECENT_WINDOW = 5

RANDOM_SEED = 20260718        # тот же seed, что в seed.py — для воспроизводимости
